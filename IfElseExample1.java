public class IfElseExample1 {
   
    public static void main(String[] args) {
        char c = 'e';
        if (c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u') {
            System.out.println(c + " is a vowel.");
        } else {
            System.out.println(c + " is not a vowel.");
        }
    }
}
